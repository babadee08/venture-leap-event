     * @param int $size
     * @return int|mixed|string
     */
    public function findLatestActivities(int $size)
    {
        return $this->createQueryBuilder('a')
            ->orderBy('a.created_at', 'DESC')
            ->setMaxResults($size)
            ->getQuery()
            ->getResult();
